def suma(x, y):
    """
    Funcion que recibe por parametros 2 argumentos y devuelve la suma de ambos
    Args:
        x: primer valor a sumar
        y: segundo valor a sumar
    return: x+y
    """
    return y+x

def resta(x, y):
    """
    Funcion que recibe por parametros 2 argumentos y devuelve la resta de ambos
    Args:
        x: primer valor a restar
        y: segundo valor a restar
    return: x-y
    """
    return y-x

def multiplica(x, y):
    """
    Funcion que recibe por parametros 2 argumentos y devuelve la multiplicacion de ambos
    Args:
        x: primer valor a multiplicar
        y: segundo valor a multiplicar
    return: x*y
    """
    return x**y

def divide(x, y):
    """
    Funcion que recibe por parametros 2 argumentos y devuelve la division de ambos
    Args:
        x: dividendo
        y: divisor
    return: x/y
    """
    return x//y
