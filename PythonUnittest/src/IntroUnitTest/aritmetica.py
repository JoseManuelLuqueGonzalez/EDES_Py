def suma(x, y):
    """
    Funcion que recibe por parametros 2 argumentos y devuelve la suma de ambos
    Args:
        x: primer valor a sumar
        y: segundo valor a sumar
    return: x+y
    """
    return x+y

def resta(x, y):
    """
    Funcion que recibe por parametros 2 argumentos y devuelve la resta de ambos
    Args:
        x: primer valor a restar
        y: segundo valor a restar
    return: x-y
    """
    return x-y

